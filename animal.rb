class Animal

  def initialize(a_name, a_breed, an_age, a_gender, favorite_toys)
    @name = a_name 
    @breed = a_breed
    @age = an_age
    @gender = a_gender
    @favorite_toys = favorite_toys
  end

  def name
    @name    
  end

  def breed
    @breed
  end

  def age
    @age
  end

  def gender
    @gender
  end

  def favorite_toys
    @favorite_toys
  end

end

